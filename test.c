#include <stdio.h>
#include <math.h>

int main() {
  for (int i = 0; i < 8; ++i) {
    for (int j = 0; j < 8; ++j) {
      printf("%f, ", cos((2. * i + 1.) * j * M_PI / (2. * 8.)));
    }
    printf("\n");
  }
}
